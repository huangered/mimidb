Node
raw_parse(const char* str) {
    Lexer lexer(str, strlen(str));
    LexToken t;
    std::vector<LexToken> data;
    while ((t = lexer.GetLexerToken()) != nullptr) {
        if (t->tok != Tok::whitespace) {
            data.push_back(t);
        }
    }
    data.push_back(EndLexToken);

    DD* node{ nullptr };
    std::stack<int> state_stack;
    std::stack<DD*> token_stack;
    std::stack<LexToken> input_stack;

    state_stack.push(0);

    for (auto iter = data.rbegin(); iter != data.rend(); iter++) {
        input_stack.push(*iter);
    }

    bool acc{};
    while (!acc) {
        bool op = eatToken(state_stack, token_stack, input_stack, &acc);

        if (!op) {
            std::cout << " no action " << std::endl;
            break;
        }
    }
    if (acc) {
        node = token_stack.top();
    } else {
        while (!token_stack.empty()) {
            node = token_stack.top();
            token_stack.pop();
            delete node;
            node = nullptr;
        }
    }
    return node->node;
}
bool
eatToken(std::stack<int>& states, std::stack<DD*>& syms, std::stack<LexToken>& input, bool* acc) {
    int curStateId = states.top();
    LexToken token = input.top();
    bool r_acc;
    bool r_state;
    int r_id;
    bool r_find{ false };
    RecordData rd = action_table[curStateId][token->tok];
    r_acc         = rd.acc;
    r_state       = rd.state;
    r_id          = rd.id;
    r_find        = (r_id != MAX_ID);
    if (r_find == true) {

        if (r_acc == true) {
            *acc = true;
            return true;
        }

        if (r_state == true) {
            states.push(r_id);
            DD* q   = new DD;
            q->node = new NodeData(token);
            syms.push(q);
            input.pop();
            return true;
        } else {
            return reduce(states, syms, r_state, r_id);
        }
    }
    return false;
}
